public class file15
{
	public static void main(String args[])
	{
		int x=100, y=200;
		System.out.println("Before sswap :  ");
		System.out.println(" x= " +x);
		System.out.println("  y=  "+y);
		int temp=x;
		x=y;
		y=temp;
		System.out.println("after sswap :  ");
		System.out.println(" x= " +x);
		System.out.println("  y=  "+y);
		
	}
}

		
		
		
		